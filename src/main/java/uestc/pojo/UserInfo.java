package uestc.pojo;

public class UserInfo {
    private int UIuserID;

    private String userName;

    private String userTrueName;

    private String userBirthday;

    private String userSex;

    private String userSchool;

    private String userEnterSchoolTime;

    private String userMajor;

    public int getUIuserID() {
        return UIuserID;
    }

    public void setUIuserID(int UIuserID) {
        this.UIuserID = UIuserID;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserTrueName() {
        return userTrueName;
    }

    public void setUserTrueName(String userTrueName) {
        this.userTrueName = userTrueName;
    }

    public String getUserBirthday() {
        return userBirthday;
    }

    public void setUserBirthday(String userBirthday) {
        this.userBirthday = userBirthday;
    }

    public String getUserSex() {
        return userSex;
    }

    public void setUserSex(String userSex) {
        this.userSex = userSex;
    }

    public String getUserSchool() {
        return userSchool;
    }

    public void setUserSchool(String userSchool) {
        this.userSchool = userSchool;
    }

    public String getUserEnterSchoolTime() {
        return userEnterSchoolTime;
    }

    public void setUserEnterSchoolTime(String userEnterSchoolTime) {
        this.userEnterSchoolTime = userEnterSchoolTime;
    }

    public String getUserMajor() {
        return userMajor;
    }

    public void setUserMajor(String userMajor) {
        this.userMajor = userMajor;
    }
}
